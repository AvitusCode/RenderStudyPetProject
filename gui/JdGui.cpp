	imGui::imGui()
	{

	}

	imGui::~imGui() {
		OnDispouse();
	}

	void imGui::OnInit()
	{
		if (status) {
			return;
		}

		IMGUI_CHECKVERSION();
		ImGui::CreateContext();
		ImGuiIO& io = ImGui::GetIO();
		io.ConfigFlags |= ImGuiConfigFlags_NavEnableKeyboard;
		io.ConfigFlags |= ImGuiConfigFlags_::ImGuiConfigFlags_DockingEnable;
		io.ConfigFlags |= ImGuiConfigFlags_::ImGuiConfigFlags_ViewportsEnable;

		const float fontSize = 18.0f;
		io.Fonts->AddFontFromFileTTF("res/fonts/OpenSans-Bold.ttf", fontSize);
		io.FontDefault = io.Fonts->AddFontFromFileTTF("res/fonts/OpenSans-Regular.ttf", fontSize);

		ImGui::StyleColorsDark();
		SetDarkThemeColors();

		auto& display = Display::getDisplay();
		ImGui_ImplGlfw_InitForOpenGL(display.getGLFWwindow(), true);
		ImGui_ImplOpenGL3_Init("#version 410");
		status = true;
	}

	void imGui::OnDispouse()
	{
		if (!status) {
			return;
		}

		ImGui_ImplOpenGL3_Shutdown();
		ImGui_ImplGlfw_Shutdown();
		ImGui::DestroyContext();
		status = false;
	}

	void imGui::GuiBegin()
	{
		ImGui_ImplOpenGL3_NewFrame();
		ImGui_ImplGlfw_NewFrame();
		ImGui::NewFrame();
	}

	void imGui::GuiEnd()
	{
		ImGuiIO& io = ImGui::GetIO();
		auto& display = Display::getDisplay();
		io.DisplaySize = ImVec2(static_cast<float>(display.getWidth()), static_cast<float>(display.getHeight()));

		ImGui::Render();
		ImGui_ImplOpenGL3_RenderDrawData(ImGui::GetDrawData());

		if (io.ConfigFlags & ImGuiConfigFlags_ViewportsEnable)
		{
			GLFWwindow* backup_current_context = glfwGetCurrentContext();
			ImGui::UpdatePlatformWindows();
			ImGui::RenderPlatformWindowsDefault();
			glfwMakeContextCurrent(backup_current_context);
		}
	}

